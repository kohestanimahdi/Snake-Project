void quarter::eat(snake &s, pair<int, int> &mm)
{
    int x=s.list::size();
    for(int i= 0 ; i< 3*x/4 ; i++)
    {
        s.pop_front();
    }

    s.push_back(mm);
    s.pop_front();
}


